package Asilos;

class Residencial {
	String nome;
	String endereco;
	int telefoneResidencial;
	int avaliacao;
	
	Residencial(String nome, String endereco, int telefoneResidencial, int avaliacao){
		this.nome = nome;
		this.endereco = endereco;
		this.telefoneResidencial = telefoneResidencial;
		this.avaliacao = avaliacao;
	}
}
